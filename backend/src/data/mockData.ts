// Helper function to find items
export const findById = (array: any[], id: string) => {
  return array.find(item => item._id === id);
};

export const findByFilter = (array: any[], filter: any) => {
  return array.filter(item => {
    return Object.keys(filter).every(key => {
      if (typeof filter[key] === 'object' && !Array.isArray(filter[key])) {
        return Object.keys(filter[key]).every(subKey => {
          return item[key] && item[key][subKey] === filter[key][subKey];
        });
      }
      return item[key] === filter[key];
    });
  });
};
